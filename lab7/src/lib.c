#include <math.h>

double func14(double x, double y, double z, double w)
{
    return (x * x * x * sin(y)) - sqrt(z) + cos(w);
}

double func18(double x, double y, double z, double w)
{
    return (x + log(y + 1)) * cos(z) - sqrt(w);
}

double func3(double x, double y, double z, double w, double v)
{
    return (exp(x) - sin(y)) * z / (cos(w) + tan(v));
}
double func8(double x, double y, double z, double w, double v)
{
    return (x * x * x + sin(y)) - z * sqrt(w) / tan(v);
}